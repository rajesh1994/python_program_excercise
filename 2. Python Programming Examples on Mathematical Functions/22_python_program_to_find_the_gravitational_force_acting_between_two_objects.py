"""
Problem Statement : Python Program to Find the Gravitational Force Acting Between Two Objects
"""

"""
Note:
Gravitational Force Formula:
    
    Every object in the universe attracts every other object. The gravitational force formula, also known as Newton's Law of Gravitation, defines the magnitude of the force between any two objects.
    
    The formula for the gravitational force includes the gravitational constant, which has a value G = 6.673 x 10^-11 N m^2/kg^2. The unit of the gravitational force is Newtons (N).
    
    Fg=(G*m1*m2)/(r**2)
    Where,
    Fg = gravitational force between two objects ()

    G = gravitational constant (G = 6.673 x 10^-11 N m^2/kg^2)

    m1 = mass of the first object (kg)

    m2 = mass of the second object (kg)

    r = distance between objects (m)
"""
print ("Problem Statement : Python Program to Find the Gravitational Force Acting Between Two Objects")

g = 6.673 * 10 ** -11
m1 = int(input("Enter the value for 'Mass of the first object': "))
m2 = int(input("Enter the value for 'Mass of the second object': "))
r = int(input("Enter the value for 'Distance between the two objects': "))

gf = round((g * m1 * m2) / (r ** 2),2)

print ("Gravitational Force Acting Between Two Objects is:", gf, "Newton")
