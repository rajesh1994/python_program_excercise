"""
Area of a Triangle Given All Three Sides:
    sqrt(s*(s-a)*(s-b)*(s-c))
    Where,
    a, b, c are the lengths of sides
    s = 1/2(a + b + c)
"""

"""
Problem Statement : Python Program to Check if a Number is a Strong Number
"""
import math
print ("Problem Statement : Python Program to Find the Area of a Triangle Given All Three Sides")

a = int(input("Enter a value for 'a':"))
b = int(input("Enter a value for 'b':"))
c = int(input("Enter a value for 'c':"))

s = (1 / 2) * (a + b + c)

area_of_triangle = round(math.sqrt(s * (s - a) * (s - b) * (s - c)), 2)
print ("Area of Triangle is:", area_of_triangle)
