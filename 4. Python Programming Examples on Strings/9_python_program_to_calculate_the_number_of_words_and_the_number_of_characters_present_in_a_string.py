"""
Problem Statement : Python program to calculate the number of words and the number of characters present in a string
"""

string = input("Enter the string: ")
c_count = 0
w_count = 1
for i in string:
    c_count = c_count + 1
    if i == " ":
        w_count = w_count + 1
print ("Number of words in the string:")
print (w_count)
print ("Number of characters in the string:")
print (c_count)
