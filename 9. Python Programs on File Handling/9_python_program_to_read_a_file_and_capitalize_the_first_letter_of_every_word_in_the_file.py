"""
Problem Statement : Python program to read a file and capitalize the first letter of every word in the file
"""

"""
Problem Solution
1. Take the file name from the user.
2. Read each line from the file and use the title() function to capitalize each word in the line.
3. Print the altered lines of the file.
5. Exit.
"""

fname = str(input("Enter a filename with .txt extension: "))
fopen = open(fname, 'r')
for line in fopen:
    capitalize = line.title()
    print (capitalize)
